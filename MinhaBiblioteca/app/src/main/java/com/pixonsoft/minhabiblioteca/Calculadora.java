public class Calculadora {

    public Double somar(double v1, double v2){

        return v1+v2;
    }

    public Double subtrair(double v1, double v2){

        return v1-v2;
    }

    public Double nultiplicar(double v1, double v2){

        return v1*v2;
    }

    public Double dividir(double v1, double v2){

        return v1/v2;
    }

}
